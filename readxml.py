def readXml():
    filepath = "C:\\Users\\I0001192\\Desktop\\Python\\Dataframe decoder prototypes\\books.xml"
    print("hello")
    tree =  ET.parse(filepath)
    root = tree.getroot()
    children = root.getchildren()
    
    columns=[]
    df = pd.DataFrame()
    
    for elem in root.iter():
      columns = columns + [elem.tag ] 
    #print(columns)
    columns = pd.Series(columns).unique()
    print(columns)
    
    #get each row
    for child in children:               
     id = [child.attrib][0]['id']
     row = [id]     
     for elem in child:
         #text = elem.text.replace('\n','')
         text = re.sub("\s\s+" , " ", elem.text)
         row = row + [text]
     #print(row)
     df = df.append(pd.Series(row),ignore_index=True)
   
    print(df)
    
    df.to_csv("books.csv")
    
    return

